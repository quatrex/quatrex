"""Parameters for the minimax rational approximation schemes.

These parameters are used in the implementation of the inverse Fermi
integrals of order -1/2 and 1/2, which are based on the minimax rational
approximation schemes described in the following papers:

- T. Fukushima, "Analytical computation of inverse Fermi-Dirac integral
of order -1/2 by piecewise rational function approximation", 2020.
- T. Fukushima, "Precise and fast computation of inverse Fermi-Dirac
integral of order 1/2 by minimax rational function approximation", 2015.

"""
from dataclasses import dataclass

import numpy as np

from qttools import NDArray

ETA_5 = 40.154967308044433594
C = 16 * ETA_5**2


@dataclass
class Coefficients:
    """Dataclass to hold coefficients for the approximation scheme.

    Attributes
    ----------
    P : NDArray
        Coefficients for the numerator polynomial.
    Q : NDArray
        Coefficients for the denominator polynomial.
    alpha : float | None
        Offset for the variable transformation.
    beta : float | None
        Scaling factor for the variable transformation.
    u_max : float | None
        Maximum value of u for which the coefficients are valid.

    """

    P: NDArray
    Q: NDArray
    alpha: float | None = None
    beta: float | None = None
    u_max: float | None = None


